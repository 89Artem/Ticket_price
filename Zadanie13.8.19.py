def main():
    # Запрашиваем количество билетов
    num_tickets = int(input("Введите количество билетов, которые вы хотите приобрести: "))

    total_cost = 0  # Общая стоимость билетов

    # Проходим по количеству билетов
    for i in range(num_tickets):
        age = int(input(f"Введите возраст посетителя для билета {i + 1}: "))  # Запрашиваем возраст

        # Определяем стоимость билета в зависимости от возраста
        if age < 18:
            cost = 0  # Бесплатно
        elif 18 <= age <= 25:
            cost = 990  # Стоимость для возраста от 18 до 25
        else:
            cost = 1390  # Полная стоимость для возраста старше 25

        total_cost += cost  # Добавляем стоимость билета к общей стоимости

    # Проверяем, есть ли скидка
    if num_tickets > 3:
        discount = total_cost * 0.10  # Скидка 10%
        total_cost -= discount  # Вычитаем скидку из общей стоимости

    # Выводим итоговую сумму к оплате
    print(f"Сумма к оплате: {total_cost} руб.")


if __name__ == "__main__":
    main()